"""Auto-comment: search posts by keyword, post AI-generated comments.

THIS IS THE HIGHEST-RISK TIER 3 FEATURE. Comments are the #1 way LinkedIn
detects inauthentic behavior. Defaults are very tight:

    daily limit: 5 comments
    hourly limit: 1 comment
    cooldown: 60-300 seconds
    min post age: 1 hour (don't pile onto fresh posts)
    max post age: 7 days (don't revive dead threads)
    min author connections: 1 (don't comment on strangers — looks like spam)
    blacklist: 30+ spammy phrases ("check out my", "DM me", "link in bio", ...)

The AI comment is generated by an injected `draft_fn(text) -> str` so the
test suite can verify the pipeline without a real LLM call. In production,
this is wired to the local drafter module.

Flow:
    search_by_keyword("agentic AI") -> [Post, ...]
    For each post:
        if post.age not in [1h, 7d]: skip
        if author not in 1st-degree network: skip
        if any blacklist phrase in post.text: skip
        draft = draft_fn(post.text)  # AI generates comment
        if any blacklist phrase in draft: skip (refuse to post)
        decision = gate.check("comment", target=urn, target_text=post.text)
        if allowed and not dry_run:
            comment_fn(urn, draft)
"""

